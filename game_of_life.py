h = gc.WINDOW_HEIGHT
s = gc.SIZE_OF_CELL

# generate the grid of cells base on windows size and cells size
game_grid = [[Cell((j * s, i * s, s, s)) for j in range(w // s)]
             for i in range(h // s)]
neighbours_grid = [[0 for j in range(w // s)] for i in range(h // s)]

# apply an initial pattern to the game
for row in range(len(init_pattern)):
    column_offset = w // s // 2 - len(init_pattern) // 2
    for column in range(len(init_pattern[row])):
        row_offset = h // s // 2 - len(init_pattern[row]) // 2
        if init_pattern[row][column] == 1:
            game_grid[row + row_offset][column + column_offset].setState(True)

framerate = 10
# While the game is not over
while not done:

    # Listen for all events
    for event in pygame.event.get():

        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True

        if event.type == pygame.KEYDOWN:
            if event.key == pygame.K_p:
                framerate = min(framerate + 1 , 30)
            if event.key == pygame.K_m:
                framerate = max(framerate - 1 , 0)
            if event.key == pygame.K_m or event.key == pygame.K_p :
                print("Frame rate is now set to {}".format(framerate))
        

    if framerate == 0 :
        continue

    screen.fill((0, 0, 0))

    # update screen and fill neighbours_grid with updated datas
    for row in range(len(game_grid)):
        for column in range(len(game_grid[row])):
            pygame.draw.rect(
                screen, game_grid[row][column].color, game_grid[row][column].rect)

            neighbours_grid[row][column] = check_alive_neighbours(
                game_grid, row, column)

    # update game_grid base on neighbours_grid
    game_grid = list(map(apply_neigbours_count, game_grid, neighbours_grid))
    pygame.display.flip()

    clock.tick(framerate)

pygame.quit()
