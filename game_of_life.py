cell_size: int = 10

matrix_margin: int = int(500 / 2 - cell_size * len(initial_state) / 2)


def show_matrix():
    """
    Show the processed matrix on the canvas
    """
    for row_index, row in enumerate(initial_state):
        for colomn_index, cell in enumerate(row):
            if cell == 1:
                rect: Tuple[int, int, int, int] = (
                    matrix_margin + colomn_index * cell_size, matrix_margin + row_index * cell_size, cell_size, cell_size)
                pygame.draw.rect(screen, (255, 255, 255), rect)


def update_matrix():
    """
    Update the current matrix with the rules of Game of Life
    """
    global initial_state
    matrix = copy.deepcopy(initial_state)

    # Iterate all the cells of the initial matrix
    for row_index, row in enumerate(initial_state):
        for column_index, _ in enumerate(row):

            total_scanned_cell: int = 0

            # Scan the 8 cells next to the current cell
            for scan_row_index in range(row_index - 1, row_index + 2):
                for scan_column_index in range(column_index - 1, column_index + 2):

                    if 0 <= scan_row_index < len(initial_state) and 0 <= scan_column_index < len(initial_state):

                        if not(scan_row_index == row_index and scan_column_index == column_index):
                            scan_cell = initial_state[scan_row_index][scan_column_index]
                            total_scanned_cell += scan_cell

            # Destroy or create a cell by his neighboor cell count
            if total_scanned_cell == 3:
                matrix[row_index][column_index] = 1
            elif total_scanned_cell <= 1 or total_scanned_cell > 3:
                matrix[row_index][column_index] = 0

    initial_state = matrix


# While the game is not over
while not done:

    screen.fill((0, 0, 0))

    show_matrix()

    pygame.display.flip()

    update_matrix()

    # Listen for all events
    for event in pygame.event.get():

        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True

    clock.tick(10)

pygame.quit()
