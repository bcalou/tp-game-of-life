def get_next_state(step: list):
    next_step = copy.deepcopy(step)
    is_alive: bool

    for line in range(len(step[0])): #line
        for column in range(len(step)): #column
            alive_neighbors: int = 0
            if step[line][column] == 1:
                #print("Cellule vivante : " + str(line) + ":" + str(column), end=" ")
                is_alive = True
            else:
                is_alive = False

            for line_offset in [line - 1, line, line + 1]:
                if line_offset < 0 or line_offset > len(step[0]) - 1: 
                    continue
                for column_offset in [column - 1, column, column + 1]:
                    #print(line_offset, column_offset, end=" ")
                    if column_offset < 0 or column_offset > len(step) - 1:
                        #print("continue")
                        continue
                    if line_offset == line and column_offset == column:
                        #print("himself")
                        continue
                    if step[line_offset][column_offset] == 1:
                        #print("add neighbor")
                        alive_neighbors += 1

            if is_alive:
                #print(alive_neighbors)
                if alive_neighbors <= 1:
                    next_step[line][column] = 0
                elif alive_neighbors <= 3:
                    next_step[line][column] = 1
                else:
                    next_step[line][column] = 0
            else:
                if alive_neighbors == 3:
                    next_step[line][column] = 1

    return next_step


# While the game is not over
while not done:

    # Listen for all events
    for event in pygame.event.get():

        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True

    #print("Update !")

    screen.fill((0, 0, 0))

    cell_size_x: int = constants.screen_size[0] // len(state)
    cell_size_y: int = constants.screen_size[1] // len(state)

    for y in range(len(state)):
        for x in range(len(state)):
            if state[y][x] == 1:
                pygame.draw.rect(
                    screen, (255, 255, 255), (x*cell_size_x, y*cell_size_y, cell_size_x, cell_size_y))
    pygame.display.flip()

    clock.tick(1)
    state = get_next_state(state)


