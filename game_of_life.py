def count_neighbor(state:list[list[int]],case_index_x:int,case_index_y:int)-> int:
    total:int = 0
    for column in range(case_index_x-1,case_index_x+2): #Find all available case in column
        if(column > -1 and column < len(state)): #Limit in the matrix
            for row in range(case_index_y-1,case_index_y+2): #Find all available case in row
                if(column != case_index_x or row != case_index_y): #Don't test the actual case
                    if(row > -1 and row < len(state[column])): #Limit in the matrix
                        if(state[column][row] == 1):
                            total+= 1
    return total
                
def cell_die(new_state,column,row): #Kill the case
    new_state[column][row]=0

def cell_born(new_state,column,row): #Give life to the case
    new_state[column][row]=1

def isAlive(state,column,row): #Show if the case is alive
    return state[column][row]==1

def get_next_state(state:list[list[int]]) -> list[list[int]]:
    new_state=copy.deepcopy(state) #Create a copy of the initial statement
    for column in range(0,len(state)):
        for row in range(0,len(state[column])):
            neighbor_count = count_neighbor(state,column,row) #Find how much neighbor the case have
            if(neighbor_count != 2 and neighbor_count != 3 and isAlive(state,column,row)): #if true the case die
                cell_die(new_state,column,row)
            elif(neighbor_count == 3 and not isAlive(state,column,row)):#if true the case born
                cell_born(new_state,column,row)
    return new_state
            

