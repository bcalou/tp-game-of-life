def draw(grid : list[list[int]] ) :
    square_size : list[int] = [screen_size[0]//len(grid), screen_size[1]//len(grid[1])]
    for i in range(len(grid))  :
        for j in range(len(grid[i])):
            if grid[i][j] == 1:
                pygame.draw.rect(screen,(0,0,0),(j*square_size[0],i*square_size[1],square_size[0], square_size[1]))
            else : 
                pygame.draw.rect(screen,(255,255,255),(j*square_size[0],i*square_size[1],square_size[0], square_size[1]))
    pygame.display.flip()


def count_neighbour(x : int, y: int, grid : list[list[int]]) -> int:
    nbr_neighbour : int = 0
    neighbour_cells = [(x - 1, y - 1), (x - 1, y    ), (x - 1, y + 1),
                       (x    , y - 1),                 (x    , y + 1),
                       (x + 1, y - 1), (x + 1, y    ), (x + 1, y + 1)]

    max_idx_x : int = len(grid) - 1
    max_idx_y : int = len(grid[1]) - 1

    for idx_x, idx_y in neighbour_cells:
        if idx_x >= 0 and idx_y >= 0 and idx_x <= max_idx_x and idx_y <= max_idx_y :
            if grid[idx_x][idx_y] == 1 :
                nbr_neighbour += 1
    return nbr_neighbour

def alive(x : int, y : int, grid : list[list[int]]) -> bool:

    if grid[x][y] == 0:
        return True if count_neighbour(x,y,grid) == 3 else False
    else:
        return False if count_neighbour(x,y,grid) > 3 or count_neighbour(x,y,grid) < 2  else True

def get_next_state(state : list[list[int]]) -> list[list[int]]:

    initial_copy :  list[list[int]] = copy.deepcopy(state)
    for i in range(len(initial_copy)):
        for j in range(len(initial_copy[i])):
            if alive(i,j,initial_copy) :
                state[i][j] = 1 
            else:
                state[i][j] = 0
    return state


