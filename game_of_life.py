def check_alive_neighbours(grid: List[List[Cell]], _row: int, _column: int) -> int:
    """
    Check for each cell in [grid[subList]] the number of neigbours and return it
    """
    alive_count = 0
    neighbour_pos = [
        (_row-1, _column-1),
        (_row-1, _column),
        (_row-1, _column+1),

        (_row, _column-1),
        (_row, _column+1),

        (_row+1, _column-1),
        (_row+1, _column),
        (_row+1, _column+1),
    ]
    for n in neighbour_pos:
        if n[0] >= 0 and n[0] < len(grid) and n[1] >= 0 and n[1] < len(grid[0]):
            alive_count += 1 if grid[n[0]][n[1]].isAlive else 0
    return alive_count

def apply_neigbours_count(game_row, neighbours_row):
    """
    used for testing survivabily on a tab of cell
    [game_row] and [neighbours_row] must have the same lenght
    """
    if len(game_row) != len(neighbours_row):
        return game_row
    for cell, neigbor_count in zip(game_row, neighbours_row):
        cell.testSurvivability(neigbor_count)
    return game_row


