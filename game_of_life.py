time = 1
print("Speed of frame is : ",time, " press Key up/down to change this value")
# While the game is not over
while not done:

    # Listen for all events
    for event in pygame.event.get():

        # Quit the infinite loop when the user presses the close button
        if event.type == pygame.QUIT:
            done = True
        
        #Detect if a key is pressed
        if event.type == pygame.KEYDOWN:
            if event.key == pygame.K_UP: #if it's the up key, time is faster
                time+= 1
                print("Speed of frame is now : ",time)
            if event.key == pygame.K_DOWN:#if it's the down key, time is slower
                if(time>1): #Security
                    time-= 1
                    print("Speed of frame is now : ",time)

    screen.fill((255, 255, 255))

    #Fill the window with cases
    for i in range(0,len(initial_state)):
        for j in range(0,len(initial_state[i])):
            #if in the matrix the case is 1, we create a life case
            if initial_state[i][j] !=0 :
                rect_life.left = int(j*screen_size[1]/len(initial_state))
                rect_life.top = int(i*screen_size[0]/len(initial_state[i]))
                screen.blit(case_life,rect_life)
            #if in the matrix the case is 0, we create a dead case
            else :
                rect_nolife.left = int(j*screen_size[1]/len(initial_state))
                rect_nolife.top = int(i*screen_size[0]/len(initial_state[i]))
                screen.blit(case_nolife,rect_nolife)

    pygame.display.flip()

    print("Update !")
    clock.tick(time)
    initial_state = get_next_state(initial_state)#Create next state

