DISPLAY_GRID = False
SCREEN_SIZE = (800, 800)
FRAME_RATE = 20
CELL_SIZE = min(int(SCREEN_SIZE[0] / len(initial_state)),
                int(SCREEN_SIZE[1] / len(initial_state[0])))


def update_screen(screen: Surface, current_state: list[list[int]]):
    screen.fill((255, 255, 255))

    for y in range(len(current_state)):
        for x in range(len(current_state[y])):
            if current_state[y][x]:               
                pygame.draw.rect(screen, (0, 0, 0), pygame.Rect(
                                                x * CELL_SIZE, y * CELL_SIZE,
                                                CELL_SIZE, CELL_SIZE))

            if DISPLAY_GRID and y == 0:
                pygame.draw.rect(screen, (0, 0, 0), pygame.Rect(
                                                x * CELL_SIZE, 0,
                                                1, SCREEN_SIZE[1]))
        if DISPLAY_GRID:
            pygame.draw.rect(screen, (0, 0, 0), pygame.Rect(
                                                0, y * CELL_SIZE,
                                                SCREEN_SIZE[0], 1))    
        pygame.display.flip()
